package main

import "strings"

// Codeforces seems to return FAILED for all contest queries with cyrillic in the name.
func containsCyrillic(s string) bool {
	alphabet := []rune{'А', 'а', 'Б', 'б', 'В', 'в', 'Г', 'г', 'Д', 'д', 'Е', 'е', 'Ë', 'ë', 'Ж', 'ж',
		'З', 'з', 'И', 'и', 'Й', 'й', 'К', 'к', 'Л', 'л', 'М', 'м', 'Н', 'н', 'О', 'о', 'П', 'п',
		'Р', 'р', 'С', 'с', 'Т', 'т', 'У', 'у', 'Ф', 'ф', 'Х', 'х', 'Ц', 'ц', 'Ч', 'ч', 'Ш', 'ш',
		'Щ', 'щ', 'Ъ', 'ъ', 'Ы', 'ы', 'Ь', 'ь', 'Э', 'э', 'Ю', 'ю', 'Я', 'я'}

	for _, r := range alphabet {
		if strings.ContainsRune(s, r) {
			return true
		}
	}
	return false
}
